Cypress.Commands.add('setToken', token => {
  localStorage.setItem('token', token)
})

Cypress.Commands.add('login', name => {
  cy.task('createToken', name).then(token => {
    cy.setToken(token)
    cy.visit('/journal/dashboard')
  })
})
